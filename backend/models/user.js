const mongoose = require('mongoose');

const userSchema = new mongoose.Schema(
  {
    email: {
      type: String,
      required: true,
      unique: true,
      lowercase: true,
      trim: true,
    },
    username: {
      type: String,
      required: true,
      unique: true,
      trim: true,
    },
    password: {
      type: String,
      required: true,
    },
    avatar: {
  type: String, 
  default: '/uploads/avatars/facteur.jpg',
},
  },
  { timestamps: true }
);

module.exports = mongoose.model('User', userSchema);